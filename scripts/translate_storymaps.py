"""Translate StoryMapJS JSON files from English to Spanish and Portuguese.

Standalone post-processing script that reads the English StoryMap JSON files
generated by build_storymaps.py and produces translated versions using the
Google Cloud Translation API.

Translatable content:
- Title/overview slide: headline, subtitle, template phrases
- Language slides: labels ("Indigenous name:", "AILLA holdings:", etc.),
  staff-authored descriptions, and template text
- Summary slide: headline, template phrases

Preserved (not translated):
- Language names (kept in original form, including indigenous names)
- ISO 639-3 codes
- URLs and AILLA links
- Numbers, dates, coordinates
- HTML structure/tags

Usage:
    # Translate both maps to both languages
    uv run scripts/translate_storymaps.py

    # Translate only Mayan map
    uv run scripts/translate_storymaps.py --family mayan

    # Translate to Spanish only
    uv run scripts/translate_storymaps.py --lang es

    # Dry run (show what would be translated, no API calls)
    uv run scripts/translate_storymaps.py --dry-run

Requires:
    - google-cloud-translate package
    - GOOGLE_APPLICATION_CREDENTIALS env var pointing to a service account key,
      OR application default credentials (gcloud auth application-default login)

Output:
    data/mayan_storymap_es.json, data/mayan_storymap_pt.json
    data/quechua_storymap_es.json, data/quechua_storymap_pt.json
    + corresponding preview HTML files
"""

