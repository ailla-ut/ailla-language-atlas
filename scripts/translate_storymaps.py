class StoryMapTranslator:
    """Translates StoryMapJS JSON files using Google Cloud Translate API.

    Uses a two-tier approach:
    1. Curated lookup dicts for labels and template phrases (consistent, free)
    2. Google Cloud Translate API for free-text descriptions (staff-authored text)

    The API is only called for text that isn't covered by the lookup dicts,
    minimizing costs and ensuring consistent terminology.
    """

    def __init__(self, dry_run: bool = False):
        self.dry_run = dry_run
        self._client = None
        self._api_call_count = 0
        self._api_char_count = 0

    @property
    def client(self) -> translate.Client:
        """Lazy-initialize the translation client."""
        if self._client is None:
            if self.dry_run:
                return None
            self._client = translate.Client()
        return self._client

    def translate_text(self, text: str, target_lang: str) -> str:
        """Translate text using the Google Cloud Translate API.

        Args:
            text: Source text in English.
            target_lang: Target language code ('es' or 'pt').

        Returns:
            Translated text.
        """
        if not text or not text.strip():
            return text

        if self.dry_run:
            print(f"    [DRY RUN] Would translate ({target_lang}): {text[:80]}...")
            return text

        self._api_call_count += 1
        self._api_char_count += len(text)

        result = self.client.translate(text, target_language=target_lang, source_language="en")
        return result["translatedText"]

    def translate_html_content(self, html: str, target_lang: str) -> str:
        """Translate HTML content, preserving tags and non-translatable elements.

        Strategy:
        1. Replace known labels via lookup dict
        2. Extract free-text segments (descriptions) and translate via API
        3. Preserve URLs, ISO codes, numbers, and HTML structure

        Args:
            html: HTML string from a StoryMap slide.
            target_lang: Target language code.

        Returns:
            Translated HTML string.
        """
        if not html:
            return html

        result = html

        # Step 1: Replace known labels
        labels = LABEL_TRANSLATIONS.get(target_lang, {})
        for en_label, translated_label in labels.items():
            result = result.replace(en_label, translated_label)

        # Step 2: Replace known template phrases (longest first to avoid
        # partial matches)
        templates = TEMPLATE_TRANSLATIONS.get(target_lang, {})
        for en_phrase, translated_phrase in sorted(
            templates.items(), key=lambda x: len(x[0]), reverse=True
        ):
            result = result.replace(en_phrase, translated_phrase)

        # Step 3: Apply regex-based replacements for short words with
        # word-boundary protection (e.g., "N items" → "N elementos")
        regex_rules = REGEX_TRANSLATIONS.get(target_lang, [])
        for pattern, replacement in regex_rules:
            result = re.sub(pattern, replacement, result)

        # Step 4: Translate remaining free-text in <p> tags that aren't just
        # labels/metadata. We look for <p> content that doesn't start with
        # <em> (those are label lines) and isn't a link-only paragraph.
        result = self._translate_description_paragraphs(result, target_lang)

        return result

    def _translate_description_paragraphs(self, html: str, target_lang: str) -> str:
        """Find and translate free-text description paragraphs.

        Identifies <p> tags containing prose descriptions (not label lines,
        not link-only paragraphs) and translates them via the API.

        Args:
            html: HTML string, possibly with labels already translated.
            target_lang: Target language code.

        Returns:
            HTML with description paragraphs translated.
        """
        # Match <p> tags that contain plain text (descriptions from AILLA)
        # Skip: <p><em>...: (label lines), <p><a href (link-only), <p><strong> (counts)
        pattern = r'<p>(?!<em>|<a |<strong>)([^<]+(?:<[^>]+>[^<]*)*)</p>'

        def replace_match(match):
            inner = match.group(1).strip()
            # Skip if it's very short or just whitespace
            if len(inner) < 10:
                return match.group(0)
            # Skip if it's mostly numbers/codes
            if re.match(r'^[\d\-\s/]+$', inner):
                return match.group(0)
            translated = self.translate_text(inner, target_lang)
            return f"<p>{translated}</p>"

        return re.sub(pattern, replace_match, html)

    def translate_headline(self, headline: str, target_lang: str) -> str:
        """Translate a slide headline.

        Language names are preserved; only template text like title-slide
        headlines get translated via the lookup dict.

        Args:
            headline: Slide headline string.
            target_lang: Target language code.

        Returns:
            Translated headline.
        """
        templates = TEMPLATE_TRANSLATIONS.get(target_lang, {})
        labels = LABEL_TRANSLATIONS.get(target_lang, {})

        # Check if it's a known title-slide headline or label
        if headline in templates:
            return templates[headline]
        if headline in labels:
            return labels[headline]

        # For language slides, the headline is "Language Name (YYYY-YYYY)"
        # — these should NOT be translated (language names stay as-is)
        return headline

    def translate_slide(self, slide: dict[str, Any], target_lang: str) -> dict[str, Any]:
        """Translate a single StoryMap slide.

        Args:
            slide: StoryMapJS slide dict.
            target_lang: Target language code.

        Returns:
            Translated copy of the slide.
        """
        translated = copy.deepcopy(slide)

        if "text" in translated:
            text_obj = translated["text"]

            if "headline" in text_obj:
                text_obj["headline"] = self.translate_headline(
                    text_obj["headline"], target_lang
                )

            if "text" in text_obj:
                text_obj["text"] = self.translate_html_content(
                    text_obj["text"], target_lang
                )

        return translated

    def translate_storymap(self, data: dict[str, Any], target_lang: str) -> dict[str, Any]:
        """Translate a complete StoryMapJS JSON structure.

        Args:
            data: English StoryMapJS JSON dict.
            target_lang: Target language code ('es' or 'pt').

        Returns:
            Translated copy of the StoryMapJS structure.
        """
        translated = copy.deepcopy(data)

        # Update the language field
        translated["storymap"]["language"] = target_lang

        # Translate each slide
        slides = translated["storymap"]["slides"]
        for i, slide in enumerate(slides):
            slide_type = "title" if i == 0 else f"slide {i}"
            if self.dry_run:
                print(f"  Translating {slide_type}...")
            slides[i] = self.translate_slide(slide, target_lang)

        return translated

    def print_stats(self) -> None:
        """Print translation API usage statistics."""
        print(f"\nAPI usage: {self._api_call_count} calls, "
              f"{self._api_char_count:,} characters")


def generate_preview_html(storymap_data: dict[str, Any], title: str,
                          output_path: Path, lang_code: str) -> None:
    """Generate a self-contained HTML preview file for a translated StoryMap.

    Args:
        storymap_data: Complete StoryMapJS JSON structure.
        title: Title for the HTML page.
        output_path: Path to write the HTML file.
        lang_code: Language code for the HTML lang attribute.
    """
    json_str = json.dumps(storymap_data, ensure_ascii=False)
    html = f"""<!DOCTYPE html>
<html lang="{lang_code}">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Preview: {title}</title>
    <link rel="stylesheet" href="https://cdn.knightlab.com/libs/storymapjs/latest/css/storymap.css">
    <style>body {{ margin: 0; padding: 0; }} #storymap {{ width: 100%; height: 100vh; }}</style>
</head>
<body>
    <div id="storymap"></div>
    <script src="https://cdn.knightlab.com/libs/storymapjs/latest/js/storymap-min.js"></script>
    <script>
        var storymap_data = {json_str};
        var storymap = new KLStoryMap.StoryMap("storymap", storymap_data, {{}});
        window.addEventListener("resize", function() {{ storymap.updateDisplay(); }});
    </script>
</body>
</html>"""

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"  Saved preview: {output_path}")


def main() -> None:
    """Translate StoryMap JSON files to Spanish and Portuguese."""
    parser = argparse.ArgumentParser(
        description="Translate StoryMapJS JSON files from English to Spanish/Portuguese."
    )
    parser.add_argument(
        "--family",
        choices=["mayan", "quechua"],
        help="Translate only this family (default: both)",
    )
    parser.add_argument(
        "--lang",
        choices=["es", "pt"],
        help="Translate to only this language (default: both)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would be translated without making API calls",
    )
    args = parser.parse_args()

    data_dir = Path(__file__).parent.parent / "data"
    project_root = Path(__file__).parent.parent

    # Determine which families and languages to process
    families = [args.family] if args.family else list(SOURCE_FILES.keys())
    languages = [args.lang] if args.lang else list(TARGET_LANGUAGES.keys())

    translator = StoryMapTranslator(dry_run=args.dry_run)

    print("AILLA Language Atlas — StoryMap Translation Pipeline")
    print(f"Families: {', '.join(families)}")
    print(f"Target languages: {', '.join(f'{code} ({name})' for code, name in TARGET_LANGUAGES.items() if code in languages)}")
    if args.dry_run:
        print("MODE: Dry run (no API calls)\n")
    else:
        print()

    for family in families:
        source_path = data_dir / SOURCE_FILES[family]
        if not source_path.exists():
            print(f"ERROR: {source_path} not found. Run build_storymaps.py first.")
            continue

        with open(source_path, "r", encoding="utf-8") as f:
            source_data = json.load(f)

        slide_count = len(source_data["storymap"]["slides"])
        print(f"{'='*60}")
        print(f"Translating {family.title()} StoryMap ({slide_count} slides)")
        print(f"{'='*60}")

        for lang_code in languages:
            lang_name = TARGET_LANGUAGES[lang_code]
            print(f"\n→ {lang_name} ({lang_code})")

            translated = translator.translate_storymap(source_data, lang_code)

            # Write translated JSON
            output_filename = SOURCE_FILES[family].replace(".json", f"_{lang_code}.json")
            output_path = data_dir / output_filename
            with open(output_path, "w", encoding="utf-8") as f:
                json.dump(translated, f, indent=2, ensure_ascii=False)
            print(f"  Saved: {output_path}")

            # Write translated preview HTML
            title_headline = translated["storymap"]["slides"][0]["text"]["headline"]
            preview_filename = f"preview_{family}_{lang_code}.html"
            preview_path = project_root / preview_filename
            generate_preview_html(translated, title_headline, preview_path, lang_code)

    translator.print_stats()

    if not args.dry_run:
        print("\nDone. Output files:")
        for family in families:
            for lang_code in languages:
                output_filename = SOURCE_FILES[family].replace(".json", f"_{lang_code}.json")
                print(f"  data/{output_filename}")
                print(f"  preview_{family}_{lang_code}.html")


if __name__ == "__main__":
    main()
